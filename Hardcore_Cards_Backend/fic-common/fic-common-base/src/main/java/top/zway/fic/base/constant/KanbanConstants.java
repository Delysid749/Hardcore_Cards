package top.zway.fic.base.constant;

/**
 * 看板业务常量类
 * 
 * 作用说明：
 * 1. 定义看板系统中的业务类型常量
 * 2. 统一管理看板和标签的类型定义
 * 3. 便于业务逻辑中的类型判断和处理
 * 
 * 设计思路：
 * - 通过常量定义代替魔法数字，提高代码可读性
 * - 集中管理业务类型，便于扩展和维护
 * - 为不同类型的看板提供差异化处理基础
 * 
 * 扩展考虑：
 * - 后续可能会增加更多看板类型
 * - 标签系统可能会有更复杂的分类
 * 
 * @author hardcore-cards
 * @since 1.0
 */
public class KanbanConstants {
    
    // ==================== 看板类型常量 ====================
    
    /**
     * 新手引导看板类型
     * 
     * 用途：标识系统为新用户自动创建的引导看板
     * 特点：
     * - 包含预置的示例内容和使用说明
     * - 帮助新用户快速了解看板功能
     * - 可能具有特殊的显示样式或行为
     * 
     * 使用场景：
     * - 用户首次注册时自动创建
     * - 在看板列表中特殊标识
     * - 可能有不同的权限控制规则
     * 
     * 数据库对应：kanban表中的type字段值为3
     */
    public static final int GUIDE_KANBAN_TYPE = 3;
    
    // ==================== 标签类型常量 ====================
    
    /**
     * 默认标签类型
     * 
     * 用途：标识普通用户创建的标准标签
     * 特点：
     * - 最常用的标签类型
     * - 用户可以自由创建、编辑、删除
     * - 支持自定义颜色和内容
     * 
     * 使用场景：
     * - 用户在卡片上添加的业务标签
     * - 用于卡片分类和状态标识
     * - 支持搜索和筛选功能
     * 
     * 扩展可能：
     * - 未来可能会有系统标签（type=2）
     * - 可能会有只读标签（type=3）
     * - 可能会有特殊权限标签（type=4）
     * 
     * 数据库对应：tag表中的type字段值为1
     */
    public static final int KANBAN_TAG_TYPE_DEFAULT = 1;
}

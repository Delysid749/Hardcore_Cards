package top.zway.fic.base.constant;

/**
 * 用户登录状态常量类
 * 
 * 作用说明：
 * 1. 定义用户登录过程中的各种状态消息
 * 2. 统一管理认证失败的错误提示信息
 * 3. 提供标准化的用户提示文案
 * 
 * 设计原理：
 * - 集中管理所有登录相关的提示消息
 * - 便于国际化和多语言支持扩展
 * - 保证用户体验的一致性
 * 
 * 安全考虑：
 * - 错误消息设计要平衡安全性和用户体验
 * - 避免泄露过多系统内部信息
 * - 为用户提供明确的解决方案指导
 * 
 * @author hardcore-cards
 * @since 1.0
 */
public class UserLoginStateConstants {

    // ==================== 成功状态消息 ====================
    
    /**
     * 登录成功提示消息
     * 
     * 使用场景：
     * - 用户名密码验证通过
     * - JWT Token生成成功
     * - 用户权限验证通过
     * 
     * 后续操作：
     * - 返回访问Token和刷新Token
     * - 记录用户登录日志
     * - 更新用户最后登录时间
     */
    public static final String LOGIN_SUCCESS = "登录成功!";

    // ==================== 认证失败相关消息 ====================
    
    /**
     * 用户名或密码错误提示消息
     * 
     * 使用场景：
     * - 用户名不存在于系统中
     * - 密码与数据库中的密码不匹配
     * - RSA解密密码失败
     * 
     * 安全设计：
     * - 不区分是用户名错误还是密码错误，避免用户名枚举攻击
     * - 统一的错误消息，不泄露具体的失败原因
     * - 建议用户检查输入信息的完整性
     * 
     * 处理建议：
     * - 提示用户检查用户名和密码的正确性
     * - 可以引导用户找回密码或重新注册
     */
    public static final String USERNAME_PASSWORD_ERROR = "用户名或密码错误!";

    // ==================== Token相关失效消息 ====================
    
    /**
     * 登录凭证过期提示消息
     * 
     * 使用场景：
     * - JWT Access Token已过期
     * - Refresh Token也已过期
     * - 用户长时间未活动导致会话超时
     * 
     * 触发条件：
     * - Token过期时间超过设定阈值
     * - 系统检测到Token时间戳无效
     * - 强制用户重新登录的安全策略
     * 
     * 用户体验：
     * - 自动跳转到登录页面
     * - 保存用户当前操作状态（如草稿）
     * - 登录后可恢复到之前的操作页面
     */
    public static final String CREDENTIALS_EXPIRED = "该账户的登录凭证已过期，请重新登录!";

    // ==================== 账户状态相关消息 ====================
    
    /**
     * 账户被禁用提示消息
     * 
     * 使用场景：
     * - 管理员手动禁用用户账户
     * - 用户违反系统使用规则被自动禁用
     * - 账户安全风险导致的临时禁用
     * 
     * 禁用原因：
     * - 恶意使用系统功能
     * - 发布不当内容
     * - 多次违规操作
     * - 安全审计发现异常行为
     * 
     * 解决方案：
     * - 联系系统管理员申请解禁
     * - 提交申诉材料说明情况
     * - 等待人工审核和处理
     */
    public static final String ACCOUNT_DISABLED = "该账户已被禁用，请联系管理员!";

    /**
     * 账户被锁定提示消息
     * 
     * 使用场景：
     * - 短时间内多次登录失败触发安全锁定
     * - 检测到异常登录行为（如异地登录）
     * - 系统安全策略触发的临时锁定
     * 
     * 锁定原因：
     * - 连续输入错误密码超过限制次数
     * - 检测到暴力破解尝试
     * - 账户安全验证失败
     * - 风控系统识别为风险账户
     * 
     * 解锁方式：
     * - 等待锁定时间自动解锁
     * - 通过邮箱验证码解锁
     * - 联系管理员手动解锁
     */
    public static final String ACCOUNT_LOCKED = "该账号已被锁定，请联系管理员!";

    /**
     * 账户已过期提示消息
     * 
     * 使用场景：
     * - 试用账户到期未续费
     * - 临时账户超过有效期
     * - 企业账户合同到期
     * 
     * 过期原因：
     * - 免费试用期结束
     * - 付费服务到期未续费
     * - 企业许可证过期
     * - 临时访问权限到期
     * 
     * 解决方案：
     * - 续费延长账户有效期
     * - 升级到正式版本
     * - 联系销售团队续约
     */
    public static final String ACCOUNT_EXPIRED = "该账号已过期，请联系管理员!";

    // ==================== 权限相关消息 ====================
    
    /**
     * 权限不足提示消息
     * 
     * 使用场景：
     * - 用户尝试访问超出权限范围的功能
     * - 角色权限不足以执行特定操作
     * - 资源访问权限验证失败
     * 
     * 权限场景：
     * - 普通用户尝试访问管理员功能
     * - 试用用户尝试使用付费功能
     * - 用户尝试操作他人的私有数据
     * 
     * 解决建议：
     * - 升级用户角色或权限
     * - 申请特定功能的访问权限
     * - 联系管理员分配相应权限
     * 
     * 安全意义：
     * - 防止越权操作
     * - 保护系统和数据安全
     * - 维护用户隐私和数据权限边界
     */
    public static final String PERMISSION_DENIED = "没有访问权限，请联系管理员!";
}